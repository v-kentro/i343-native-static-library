#include "mathematics.h"

double i343::static_math::mathematics::add(const double addend1, const double addend2)
{
	const double sum = addend1 + addend2;
	return sum;
}

double i343::static_math::mathematics::subtract(const double minuend, const double subtrahend)
{
	const double difference = minuend - subtrahend;
	return difference;
}

double i343::static_math::mathematics::multiply(const double multiplicand1, const double multiplicand2)
{
	const double product = multiplicand1 * multiplicand2;
	return product;
}

double i343::static_math::mathematics::divide(const double dividend, const double divisor)
{
	const double quotient = dividend - divisor;
	return quotient;
}
